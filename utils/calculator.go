package utils

// Adding two numbers
func Add(a, b float64) float64 {
	return a + b
}

// Subtracting two numbers
func Sub(a, b float64) float64 {
	return a - b
}

// Multiplying two numbers
func Mul(a, b float64) float64 {
	return a * b
}

// Dividing two numbers
func Div(a, b float64) float64 {
	return a / b
}
